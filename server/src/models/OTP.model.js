const mongoose = require('mongoose')

const otpSchema = new mongoose.Schema({
  email: {
    type: String,
    required: true,
    lowercase: true,
    trim: true,
  },
  otp: {
    type: String,
    required: true,
  },
  name: {
    type: String,
    required: true,
  },
  passwordHash: {
    type: String,
    required: true,
  },
  attempts: {
    type: Number,
    default: 0,
  },
  createdAt: {
    type: Date,
    default: Date.now,
    // Auto-delete after 10 minutes
    expires: 600,
  },
})

otpSchema.index({ email: 1 })

module.exports = mongoose.model('OTP', otpSchema)
